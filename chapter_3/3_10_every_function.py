family_members = ['cynthia', 'lucas', 'papi', 'mami', 'ralph', 'brian', 'jennifer']

print(f"sorted list {sorted(family_members)}")

print(f"sorted list in reverse {sorted(family_members, reverse = True)}")

family_members.reverse()
print(f"reveresed list {family_members}")

family_members.sort()
print(f"permanently sorted list {family_members}")
